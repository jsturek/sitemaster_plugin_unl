<h2>To edit your self reported progress:</h2>
<ol>
    <li>Log into http://webaudit.unl.edu/ if you are not already.</li>
    <li>After logging in, you will be presented with a list of sites that you are a member of.  In this list, find the site that you want to edit and click on it.</li>
    <li>You will see the latest report that we have generated for your site.  The first section of the report is titled “UNLedu 4.0 Progress Report”.</li>
    <li>There will be an button at the bottom of this section to “Edit self reported progress”.  Click the button.  If you do not see the button, then you either need to log in, or you do not have permission to edit the self reported progress.  You need to be an administrator of the site in webaudit in order to edit self reported progress.</li>
    <li>Fill out the form, and click “save” when you are done.</li>
</ol>

<h2>To edit your site title:</h2>
<ol>
    <li>Follow steps 1-2 of “To edit your self reported progress”</li>
    <li>Click “Edit Site Info” under the webaudit navigation for the selected site.  You will need to be an administrator of the site in webaudit in order to edit this information.</li>
    <li>Fill out the form and click “save” when you are done.</li>
</ol>

<h2>To Register a site:</h2>
<ol>
    <li>If your site is not registered in webaudit and it is not a UNLcms site, you can do so by logging into webaudit and adding it manually.</li>
    <li>If the site is in UNLcms, it and all administrators in UNLcms will be synced at midnight every night.  Only administrator roles will be brought into webaudit automatically from UNLcms.  If you want a non-admin member to get webaudit notifications or for them to be able to run manual scans, you will have to manually add them to the site in webaudit.</li>
</ol>

<h2>To Join a site:</h2>
<ol>
    <li>You can request to join any site.  An administrator for that site in webaudit will need to approve your request, or you will need to prove that you have physical access to make changes on the site.  Follow on-screen instructions for this process.</li>
</ol>

